package junit;

public class Calculator {
	
	public int add(int val1,int val2){
		return val1+val2;
	}

	public int subtract(int val1,int val2){
		return val1-val2;
	}
	public float divide(int val1,int val2){
		return val1/val2;
	}
	public int multiply(int val1,int val2){
		return val1*val2;
	}
	
	
	
	
}
